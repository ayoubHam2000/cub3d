#include "cub3d.h"

int	is_tile(t_map *map, int x, int y)
{
	if (y < 0 || x < 0 || y >= map->len || x >= ft_strlen(map->map[y]))
		return (1);
	if (map->map[y][x] == '1')
		return (1);
	return (0);
}
